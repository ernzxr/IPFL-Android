package calcularduracion;

public class CalcularDuracion {

    public static void main(String[] args) {
        int inicio, fin, duracion;
        
        inicio = 20;
        fin = 23;
        
        duracion = fin - inicio;
        
        // show result
        
        System.out.println("La funcion dura: "+duracion+" horas.");
    }
    
}
